    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed

        String titulo = txtTitulo.getText();
        String mes = txtMes.getText();
        String dia = txtDia.getText();
        String anio = txtAnio.getText();
        int validarDia = Integer.parseInt(dia);
        int validarMes = Integer.parseInt(mes);

        if (titulo.trim().equals("")) {
            lblMensaje.setText("LLENE LOS RECUADROS!");
            lblMensaje.setVisible(true);
            txtTitulo.requestFocus();
        } else if (mes.trim().equals("") || dia.trim().equals("") || anio.trim().equals("")) {
            txtmsj.setText("LLENE LOS RECUADROS!");
            txtmsj.setVisible(true);
        } else if (!numberValidator(mes.trim()) || !numberValidator(dia.trim()) || !numberValidator(anio.trim())) {
            txtmsj.setText("SOLO NUMEROS!");
            txtmsj.setVisible(true);
        } else if (validarDia > 31 || validarDia <= 0 || validarMes > 12 || validarMes <= 0) {
            txtmsj.setText("FORMATO DE FECHA INCORRECTO!");
            txtmsj.setVisible(true);
        } else {
            registrarPrograma();
            try {
                ListDataPrograma2 list = new ListDataPrograma2();
                MainPrograma2.dpView.add(list);
                list.toFront();
                list.setVisible(true);
            } catch (ClassNotFoundException | SQLException ex) {
                Logger.getLogger(regPrograma2.class.getName()).log(Level.SEVERE, null, ex);
            }
            setActionsPrograma2 menu = new setActionsPrograma2();
            MainPrograma2.dpMenu.add(menu);
            menu.toFront();
            menu.setVisible(true);
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

