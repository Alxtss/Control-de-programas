    private void txtMesKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtMesKeyTyped
        String m = txtMes.getText();
        int limite = 2;
        if (m.length() == limite) {
            evt.consume();
            if (!numberValidator(m.trim())) {
                txtmsj.setText("SOLO NUMEROS!");
                txtmsj.setVisible(true);
            } else {
                txtDia.requestFocus();
            }
        }

    }//GEN-LAST:event_txtMesKeyTyped

